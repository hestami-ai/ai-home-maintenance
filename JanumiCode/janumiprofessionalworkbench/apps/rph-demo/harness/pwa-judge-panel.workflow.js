// Layer B of the virtuous-cycle validation harness — a Claude multi-lens JUDGE PANEL that scores whether a
// generated PWA graph is a FAITHFUL interpretation of the prompt (the semantic question the deterministic
// invariant layer cannot answer). It reads the canonical export + the agent's own recorded plan (NOT a screenshot,
// NOT the Svelte-Flow render model) through four independent reviewer lenses, then synthesises a consensus verdict.
//
// This runs in the Workflow tool's runtime (globals: agent / parallel / phase / log / args) — it is NOT Node code
// and is excluded from the lint/type gate. Invoke it AFTER a live generation run has written the gallery artifacts:
//
//   Workflow({
//     scriptPath: 'apps/rph-demo/harness/pwa-judge-panel.workflow.js',
//     args: {
//       exportPath: 'apps/rph-demo/e2e-results/gallery/<test-slug>/graph.export.json',
//       truthPath:  'apps/rph-demo/e2e-results/gallery/<test-slug>/engine-truth.truth.json',
//       prompt:     '<the NL prompt the PWA was generated from>'
//     }
//   })
//
// Returns { verdicts:[per-lens], consensus:{verdict, meanScores, topGaps, recommendation} }.
export const meta = {
	name: 'pwa-judge-panel',
	description: 'Multi-lens Claude judge panel: does the generated PWA graph faithfully interpret the prompt?',
	phases: [
		{ title: 'Judge', detail: '4 independent lensed reviewers score the export' },
		{ title: 'Synthesize', detail: 'consensus verdict + merged gaps' }
	]
};

const exportPath = args.exportPath;
const truthPath = args.truthPath;
const prompt = args.prompt;

const VERDICT = {
	type: 'object',
	additionalProperties: false,
	required: ['scores', 'verdict', 'gaps', 'rationale'],
	properties: {
		scores: {
			type: 'object',
			additionalProperties: false,
			required: ['vModel', 'userCenteredDesign', 'jobsToBeDone', 'coherence'],
			properties: {
				vModel: { type: 'number' },
				userCenteredDesign: { type: 'number' },
				jobsToBeDone: { type: 'number' },
				coherence: { type: 'number' }
			}
		},
		verdict: { type: 'string', enum: ['faithful', 'partial', 'poor'] },
		gaps: { type: 'array', items: { type: 'string' } },
		rationale: { type: 'string' }
	}
};

const LENSES = [
	{ key: 'vmodel', persona: 'a systems-engineering reviewer expert in the V-model (paired definition <-> verification/validation across the left and right sides, requirement traceability, integration levels)' },
	{ key: 'ucd', persona: 'a User-Centered Design reviewer (user needs, personas, journeys, usability, translating needs into observable product behaviour)' },
	{ key: 'jtbd', persona: 'a Jobs-To-Be-Done product reviewer (customer jobs, desired outcomes, and need/opportunity discovery framing the work up front)' },
	{ key: 'architect', persona: 'a holistic software-architecture reviewer judging decomposition quality, coherence, and completeness (intent -> delivery -> baseline) and data-flow sanity' }
];

const ground = `A PWA is a reusable software-engineering work architecture: a HIERARCHY of PWU Types (the "permits" edges = decomposition / part-of) with DATA-FLOW between them (a producer's requiredOutput artifact feeds a consumer's requiredInput). It was generated by an AI agent from this exact prompt:\n\n"${prompt}"\n\nRead the canonical graph export at ${exportPath} (nodes with pwuKind / requiredInputs / requiredOutputs, plus permits, dataFlow, artifacts, roots). Also read ${truthPath} — the agent's OWN recorded plan is under conversations[].state.entries (role USER = the prompt; role AGENT = its narration + tool calls). Judge how well the GENERATED graph is a reasonable, faithful, and COMPLETE interpretation of the prompt — not merely whether it is well-formed.`;

phase('Judge');
const judged = await parallel(
	LENSES.map((l) => () =>
		agent(
			`You are ${l.persona}, acting as an independent reviewer of a generated PWA.\n\n${ground}\n\nScore each dimension 0.0-1.0: vModel (does the structure reflect V-model V&V pairing + traceability?), userCenteredDesign (is user-need / behaviour definition explicit?), jobsToBeDone (is JTBD discovery present and up front?), coherence (is it a coherent, complete SDLC decomposition from intent to baseline?). Then list concrete GAPS (things the prompt asked for that are missing or weak), an overall verdict, and a brief rationale citing specific node names from the export.`,
			{ schema: VERDICT, label: `judge:${l.key}`, phase: 'Judge', effort: 'high' }
		).then((v) => ({ lens: l.key, ...v }))
	)
);
const verdicts = judged.filter(Boolean);

phase('Synthesize');
const CONSENSUS = {
	type: 'object',
	additionalProperties: false,
	required: ['verdict', 'meanScores', 'topGaps', 'recommendation'],
	properties: {
		verdict: { type: 'string', enum: ['faithful', 'partial', 'poor'] },
		meanScores: {
			type: 'object',
			additionalProperties: false,
			required: ['vModel', 'userCenteredDesign', 'jobsToBeDone', 'coherence'],
			properties: {
				vModel: { type: 'number' },
				userCenteredDesign: { type: 'number' },
				jobsToBeDone: { type: 'number' },
				coherence: { type: 'number' }
			}
		},
		topGaps: { type: 'array', items: { type: 'string' } },
		recommendation: { type: 'string' }
	}
};
const consensus = await agent(
	`You are the lead reviewer. Aggregate these ${verdicts.length} independent lensed verdicts on a generated SDLC PWA into a consensus. Compute mean scores per dimension, a consensus verdict (majority; if split, the more critical wins), the TOP merged gaps (dedup, most important first), and a one-paragraph recommendation on whether the agent reasonably interpreted the prompt "${prompt}".\n\nVERDICTS:\n${JSON.stringify(verdicts, null, 2)}`,
	{ schema: CONSENSUS, phase: 'Synthesize', effort: 'high' }
);

log(`consensus=${consensus.verdict} vModel=${consensus.meanScores.vModel} UCD=${consensus.meanScores.userCenteredDesign} JTBD=${consensus.meanScores.jobsToBeDone} coherence=${consensus.meanScores.coherence}`);
return { verdicts, consensus };
